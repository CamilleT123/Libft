#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (!lst || !del)
		return;
	del(lst->content);
	free(lst);
	lst = NULL; // la mémoire de ’next’ ne doit pas être free.
}